var addBorder=require('./addBorder');
describe('addBorder',function(){
    it("should return correctly", function(){
        expect(addBorder(["abc",
            "ded"])).toEqual(["*****",
            "*abc*",
            "*ded*",
            "*****"]);
        expect(addBorder(["a"])).toEqual(["***",
            "*a*",
            "***"]);
        expect(addBorder(["aa",
            "**",
            "zz"])).toEqual(["****",
            "*aa*",
            "****",
            "*zz*",
            "****"]);
        expect(addBorder(["abcde",
            "fghij",
            "klmno",
            "pqrst",
            "uvwxy"])).toEqual(["*******",
            "*abcde*",
            "*fghij*",
            "*klmno*",
            "*pqrst*",
            "*uvwxy*",
            "*******"]);
        expect(addBorder(["wzy**"])).toEqual(["*******",
            "*wzy***",
            "*******"]);
    });
});
